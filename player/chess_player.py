from abc import ABC, abstractmethod


class ChessPlayer(ABC):
    @abstractmethod
    def __init__(self, color):
        self.__color = color

    @property
    def color(self):
        return self.__color

    @color.setter
    def color(self, c):
        self.__color = c

    @abstractmethod
    def move(self, board):
        pass
